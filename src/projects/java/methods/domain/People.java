package projects.java.methods.domain;

public class People {
    private String name;
    private int years;

    public void setName(String name) {
        this.name = name;
    }

    public void setYears(int years) {
        this.years = years;
    }

    public String getName() {
        return this.name;
    }

    public int getYears() {
        return this.years;
    }
}
